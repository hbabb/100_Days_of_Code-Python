# Subscripting
print("Hello"[0])
print("Hello"[-2])
print("Hello"[-1])

# String
print("123" + "345")

# Integer = Whole number
print(123 + 345)

# Large Integers
print(123_456_789)

# Float = Floating Point Number
print(3.24259)

# Boolean
print(True)
print(False)

street_name = "Abbey Road"
print(street_name[4] + street_name[7])